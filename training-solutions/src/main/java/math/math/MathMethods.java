package math.math;

public class MathMethods {

    public static void main(String[] args) {

        System.out.println(Math.max(20,10));
        System.out.println(Math.min(20,10));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(10.20));
        System.out.println(Math.abs(-300.3));
        System.out.println(Math.negateExact(1));
        System.out.println(Math.addExact(10,20));
        System.out.println(Math.subtractExact(10,20));
        System.out.println(Math.multiplyExact(10,10));
        System.out.println(Math.pow(2.2,2.2));
        System.out.println(Math.incrementExact(5));
        System.out.println(Math.decrementExact(1));
        System.out.println(Math.random());
    }
}
